// 十进制
const num1 = 100
// b => binary
/*
* 0 * 2**0 + 0 * 2**1 + 1 * 2**2 = 4
* */
const num2 = 0b100
// o => octal
/*
* 0 * 8**0 + 0 * 8**1 + 1 * 8**2 = 64
* */
const num3 = 0o100
// x => hex
// 0 * 16**0 + 0 * 16**1 + 0 * 16**2 = 256
const num4 = 0x100

// 100 4 64 256
console.log(num1, num2, num3, num4)




